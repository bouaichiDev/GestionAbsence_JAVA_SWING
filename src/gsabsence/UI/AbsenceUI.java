    public void seletedAbsence() {
        int selectedRow = jTableAbsemce.getSelectedRow();
        // Vérifier qu'une ligne est sélectionnée
        if (selectedRow == -1) {

            return;
        }

        // Récupérer l'ID (1ère colonne = index 0)
        int idAbsence = Integer.parseInt(
                    jTableAbsemce.getValueAt(selectedRow, 0).toString()
        );

        // Récupérer l'employé depuis la BD
        AbsenceService es = new AbsenceService();
        Absence absence = es.findById(idAbsence);

        if (absence != null) {
            System.out.println("Employé sélectionné : " + absence.getEmploye().getNom());

            currentAbsenceId = absence.getId();
            employeSelect.setSelectedItem(absence.getEmploye());

            typeAbcenseSelect.setSelectedItem(absence.getTypeAbsemce());
            dateFrom.setDate(absence.getDateDebut());
            dateTo.setDate(absence.getDateFin());
        } else {
            currentAbsenceId = 0;
            JOptionPane.showMessageDialog(this,
                        "Employé introuvable",
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE
            );
        }
    }

    public void loadAbsence() {

        model.setRowCount(0);
        for (Absence e : absenceService.findAll()) {
            model.addRow(new Object[]{
                e.getId(),
                e.getEmploye().getNom(),
                e.getTypeAbsemce().getLibelle(),
                e.getDateDebut() + " => " + e.getDateFin()
            });
        }
    }

    public void loadAbsenceFilterred(int employe) {

        model.setRowCount(0);
        for (Absence e : absenceService.findAllByEmplye(employe)) {
            model.addRow(new Object[]{
                e.getId(),
                e.getEmploye().getNom(),
                e.getTypeAbsemce().getLibelle(),
                e.getDateDebut() + " => " + e.getDateFin()
            });
        }
    }

    public void loadTypeAbsence() {

        for (TypeAbsence t : typeAbsenceService.findAll()) {
            typeAbcenseSelect.addItem(t);
        }
    }

    public void loadEmploye() {
        for (Employe e : employeSrvice.findAll()) {
            employeSelect.addItem(e);
            employeSelectFiltrre.addItem(e);
        }
    }

