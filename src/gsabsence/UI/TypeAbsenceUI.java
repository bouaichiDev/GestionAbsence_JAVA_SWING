    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        TypeAbsence e = new TypeAbsence(typeAbsenceTxt.getText(), justification.getText());
        boolean isAdded = false;
        isAdded = absenceService.create(e);
        if (isAdded) {
            JOptionPane.showMessageDialog(
                        this,
                        "TypeAbsence " + e.getLibelle() + " bien ajouter",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE
            );
            clearForm();
            loadTypeAbsence();
        } else {
            JOptionPane.showMessageDialog(
                        this,
                        "Erreur ajoute de " + e.getLibelle(),
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (currentTypeId > 0) {
            TypeAbsence e = new TypeAbsence(currentTypeId, typeAbsenceTxt.getText(), justification.getText());
            boolean isUpdated = false;
            isUpdated = absenceService.update(e);
            if (isUpdated) {
                JOptionPane.showMessageDialog(
                            this,
                            "TypeAbsence " + e.getLibelle() + " bien modifier",
                            "Success",
                            JOptionPane.INFORMATION_MESSAGE
                );
                clearForm();
                loadTypeAbsence();
            } else {
                JOptionPane.showMessageDialog(
                            this,
                            "Erreur modification de " + e.getLibelle(),
                            "Erreur",
                            JOptionPane.ERROR_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(
                        this,
                        "Erreur auccun TypeAbsence a ete selectionner ",
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (currentTypeId > 0) {
            TypeAbsence e = new TypeAbsence(currentTypeId, typeAbsenceTxt.getText(), justification.getText());
            boolean isDeleted = false;
            isDeleted = absenceService.delete(e);
            if (isDeleted) {
                JOptionPane.showMessageDialog(
                            this,
                            "TypeAbsence " + e.getLibelle() + " bien supprimer",
                            "Success",
                            JOptionPane.INFORMATION_MESSAGE
                );
                clearForm();
                loadTypeAbsence();
            } else {
                JOptionPane.showMessageDialog(
                            this,
                            "Erreur supression de " + e.getLibelle(),
                            "Erreur",
                            JOptionPane.ERROR_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(
                        this,
                        "Erreur auccun TypeAbsence a ete selectionner ",
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton3ActionPerformed

