    public void loadTypeAbsence() {
        model.setRowCount(0);
        for (TypeAbsence e : absenceService.findAll()) {
            model.addRow(new Object[]{
                e.getId(),
                e.getLibelle(),
                e.getJustification()
            });
        }
    }

    public void seletedType() {
        int selectedRow = jTableTypeAbsence.getSelectedRow();

        // Vérifier qu'une ligne est sélectionnée
        if (selectedRow == -1) {

            return;
        }

        // Récupérer l'ID (1ère colonne = index 0)
        int idTypeAbsence = Integer.parseInt(
                    jTableTypeAbsence.getValueAt(selectedRow, 0).toString()
        );

        // Récupérer l'employé depuis la BD
        typeAbsenceService es = new typeAbsenceService();
        TypeAbsence type = es.findById(idTypeAbsence);

        if (type != null) {
            System.out.println("TypeAbsence sélectionné : " + type.getLibelle());

            currentTypeId = type.getId();
            typeAbsenceTxt.setText(type.getLibelle());
            justification.setText(type.getJustification());
            if (type.getJustification().equals("Oui")) {
                System.out.println("getJustification Oui : " + type.getJustification());
                justification.setSelected(true);
            } else {
                System.out.println("getJustification Non : " + type.getJustification());
                justification.setSelected(false);
            }
        } else {
            currentTypeId = 0;
            JOptionPane.showMessageDialog(this,
                        "TypeAbsence introuvable",
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE
            );
        }
    }

