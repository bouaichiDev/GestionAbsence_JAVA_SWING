    private void employeSelectFiltrreItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_employeSelectFiltrreItemStateChanged
        if (evt.getStateChange() != ItemEvent.SELECTED) {
            return;
        }

        Object item = evt.getItem();

        // Case 1: "All / Empty" → load all absences
        if (!(item instanceof Employe)) {
            loadAbsence();
            return;
        }

        // Case 2: real Employe
        Employe e = (Employe) item;
        System.out.println(e.getNom());
        loadAbsenceFilterred(e.getId());
    }//GEN-LAST:event_employeSelectFiltrreItemStateChanged

